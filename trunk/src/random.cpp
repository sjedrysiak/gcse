#include "random.h"
#include <QtGlobal>
#include <cstdlib>

//returns random integer from 0 to n-1
int Random::rand(unsigned int n)
{
    return Random::rand() * n;
}

//returns random double from [0,1)
double Random::rand()
{
    return qrand() / (RAND_MAX + 1.0);
}
