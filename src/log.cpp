#include "log.h"
#include <QFile>
#include <QTextStream>

void Log::log(const QString& str, const QString& path)
{
    QFile file(path);
    if (!file.open(QIODevice::Append | QIODevice::Text))
        return;
    QTextStream out(&file);
    out.setCodec("UTF-8");
    out << str;
    file.close();
}
